Върколака (вълколак, влакодлак, вуколак) се появява в славянската митология някъде към 14век. , когато цяла европа е облада от появата на митологични чудовища. Върколага представлява човек обладан от зли духови, които при пълнолуние се превръщат във вълкочовек. Друг начин широко разпространен за превръщането на човек във вълк е ако бъде дран от вампир ако не умре на следващото пълнолуние той се превръща във вълк. Върколаците живеят в изоставени воденици, ханове, плевници и край кръстопътища, далеч от села и градове. Който мине покрай обиталищата им, те го нападат, удушават, изпиват му кръвта или направо го изяждат. Върколаците могат да примамят и прелъстят жени, да встъпят в полово сношение с тях, в резултат на което се раждат деца, които нямат хрущял на носа, виждат злите духове и имат свръхестествени способности (виж сдухач). Сред българите и сърбите се вярвало, че върколаците предизвикват слънчеви и лунни затъмнения, отхапвайки част от небесното светило. Единствения начин да се убие върколак се смята забиването на кол в сърцето му. 


