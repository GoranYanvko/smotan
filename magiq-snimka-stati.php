За да избегнете неприятности, постарайте се никога да не давате свои снимки на слабо познати хора. Не бързайте и да изхвърляте фотографии, защото могат да попаднат у ваши недоброжелатели. Съвременните магове използват достиженията на научно-техническия прогрес, за да осъществяват своите магьоснически дела. И ако в древността за нанасяне на вреда използвали дървена (или восъчна) кукла и забивали по нея игли, то днес ролята на кукла може да изпълни една снимка. Нещо повече, по снимката може да се разбере какво е здравето, личният живот и бъдещето на човека. Едва ли ще искате някой не само да бъде посветен в подробностите на вашия живот, но и да ги коригира. 
Понякога ние сами, по-скоро от незнание, си нанасяме вреда. 
За да не се случва това, трябва да запомним едно просто правило: никога не бива да късаме и още по-малко да режем снимки. Защо? Както е известно, обвивката на човек е много по-голяма, отколкото физическото му тяло. Когато някой се фотографира, с това разширява пространството си, включвайки и областта на снимката. Когато късаме снимката, грубо разрушаваме взаимовръзката на човека с неговото изображение на енергийно равнище, което понякога води до горчиви последствия. 
