<b>Какво е вуду?</b></br> 
Разхождащи се зомбита, кървави жертвоприношения, олтари с черепи, обсебени, нашепващи страховити и неразбираеми слова. Свещи от човешка лой, зли духове и демони, кукли, набодени с игли, страховити маски, обесени кози и обезглавени петли, гердани от пилешки крака, кървави кръстове... типични вуду сцени... характерни обаче за холивудски трилър, нищо повече. 
Седемдесет и два милиона души по света практикуват вуду като религия и магия. Какво ги привлича? Разбирането за първичните сили на майката природа, водещо началото си от черна Африка, и възможността чрез тях човек сам да управлява живота и съдбата си. Тези знания обаче могат да бъдат доста опасни и затова е препоръчително да се усвояват с учител. Но експертите от Училище “МАГ” са адаптирали някои от вуду техниките и ритуалите, които всеки един човек може да изпълни в дома си, без да се намалява ефективността на магията. 
Подобни вуду комплекти се продават в много ботаники (магазинчета за вуду принадлежности) по света. Вече и в България, чрез редакцията на вестник ВРАЧКА, всеки желаещ може да изпълни най-съкровените си желания, да хармонизира живота си, да постигне любов, успех и просперитет. 
<br /><br />
<b>Вуду по света </b><br />
Вуду е възникнала на Карибските острови религия, известна още като вуудуу и хуудуу. Корените на религията са в Западна Африка, откъдето вярването се е пренесла заедно с превозваните роби. Думата “вуду” произхожда от воду, което означава дух или божество в превод от езика фон, едно от наречията на догони – племе, живеещо в район на Западна Африка, където именно се намира домът на вуду божествата – Ориша и духовете Лоа. Смесването на традиционните вярвания на Йоруба и католическите церемонии довело до формирането на тази религия. Трябва да добавя, че освен Йоруба влияние оказват и други племена от Западна Африка, като най-голямо е на племето догони, което според легендите произлиза от хора, дошли от съзвездието Сириус. Освен под името вуду в различните части на Америка съществуват и други наименования като сантерия, макумба, кандобле и т.н. 
Във всички тези религиозно-магически култове обаче се правят сходни ритуали и се използват едни и същи богове и духове с различни имена. 
Въпреки че се е оформила на американския континент, в основата си вуду е африканска по своя произход. Най-характерното, описано от изследователите на африканските традиции, е присъствието на танца, музиката, словото, ритъма в живота на всеки, независимо към кое племе принадлежи. Оттук и магията на Африка е магия на словото, заклинанието и музиката. За африканския маг всеки предмет, животно или човек излъчва невидими за обикновения човек набор от звуци, които представляват неговото слово. Магията на Африка е природна магия и освен със силите на ритъма, словото и музиката се характеризира и с магията на изображенията. 
<br /><br />
<b>Ролята на куклите с иглите</b><br />
Mагията вуду е любима на сценаристите на трилъри. Благодарение на тях всеки, който чуе за вуду, си представя човек, бодящ кукла с игли, или страховито зомби. Във вуду този метод за работа се използва за концентриране върху определена цел (най-често за лечение - иглите се поставят върху определени точки, извършва се нещо като дистанционна акупунктура и за привличане на определени природни сили на помощ). Куклата играе роля на посредник, тя представлява енергийно копие на човека, върху който се въздейства. Благодарение на това си свойство чрез тях могат да се правят ритуали и магии за пари, късмет, привличане на здраве, за любов, за бизнес просперитет, за отмъщение и наказание, за защита. 
<br /><br />
<b>Приложение на вуду</b><br /> 
Вуду е приемана от всички за една от най- древните и първични по своята същност магии. Тя е в природата на всеки човек и всеки може да я прилага ефективно, стига да направи съответните практически постъпления.
