<!-- End easytrader --><h5>Целувката спасява от бръчки</h5>

<img src="stati/kiss.jpg" width="180" height="200" alt="celuvka" border="0" />

Учените откриха нова дрога, която е 200 пъти по-силна от морфина. Силният наркотик се произвежда по много прост начин – по време на целувка. Тялото на целуващите се хора произвежда вещества със свойства, подобни на наркотичните, но с много по-силно въздействие, пише в. “Стампа”.<br /><br />
Дори най-ефективният крем срещу бръчки не може да спаси появата на бръчки по лицето, а целувката е в състояние да ги забави. Доказано е, че при всяка целувка участват 29 лицеви мускула и тя е отлична профилактика срещу бича за всяка жена – бръчките, пише вестник “Новинар”.<br /><br />
Учените са установили, че неуверените влюбени целуват партньорите си с отворени очи, за да видят какъв е ефектът от целувката им. 66 % от хората в света затварят очи, когато се целуват.<br />
Мъжът, който всеки ден целува съпругата си, преди да отиде на работа, живее с 5 години по-дълго от останалите. Затова специалисти съветвали клиентите си от мъжки пол да приемат целувката сутрин като служебно задължение, ако искат да поживеят по-дълго. <br /><br />
Учени са установили, чецелувката помага на жените да се преборят със стреса.<br /><br />
Приблизително 20 160 секунди, или две седмици от живота на човека минават в целувки. 
Целувката, продължила 60 секунди, изгаря 26 калории. При бърза целувка се изгарят само 2-3 калории. Жителите по света се целуват за пръв път приблизително на 14-годишна възраст.<br />
