<h4>ОВЕН</h4>
Инпулсивна, страстна и изразителна. Пълна с енергия. Вечно търсеща авантюри. Нейният парфюм трябва да бъде предизвикателен, но не много силен. Жасмин, тубероза, мускус. 
<br /><br />
<h4>ТЕЛЕЦ</h4>
Остроумна и чувствителна. Упорита и с беспогрешна интуиция. Подходящ е парфюм с лека острота, с неангажираща миризма. 
<br /><br />
<h4>БЛИЗНАЦИ</h4>
Жена с много приятели и с много силно развито въображение. Не понася самотата. иска да постигне всичко. Нейният парфюм е с остра и натрапчива миризма. 
<br /><br />
<h4>РАК</h4> 
Романтична, чувствителна, мечтателка. Парфюмът и е с мирис на мъхести храсти, сандалово дърво и ванилия. 
<br /><br />
<h4>ЛЪВ</h4>
Тя прекалява във всичко, заредена е с ентусиазъм, революционерка, страстна. Обича да рискува и да провокира. Не обича да бъде пренебрегната. Парфюмът и трябва да бъде силен, с необикновен аромат - градина, пълна с екзотични цветя. 
<br /><br />
<h4>ДЕВА</h4>
Организирана, пресметлива. Изненадваща в любовта. Подходящ парфюм - със силен, упойващ аромат. 
<br /><br />
<h4>ВЕЗНИ</h4> 
Женствена. Предпочита лек с свеж парфюм. 
<br /><br />
<h4>СКОРПИОН</h4>
Разточителна, темпераментна. Има вродена склонност да предизвиква. Всеки парфюм и подхожда. 
<br /><br />
<h4>СТРЕЛЕЦ</h4>
Авантюриска, идеалистка. Обича пътешествията. Откривателка. Аромат на изсъхнали цветя се носи от нейния парфюм. 
<br /><br />
<h4>КОЗИРОГ</h4>
Елегантна. Нуждае се от везхищение. Нейния парфюм е специален, с мирис на роза и момина сълза. 
<br /><br />
<h4>ВОДОЛЕЙ</h4>
Вярна, непредвидима. Ненатрапчив и свеж парфюм. 
<br /><br />
<h4>РИБИ</h4>
Жена - творец. Идеален приятел, изключителна майка. Има нужда от мирис на мускус. 


